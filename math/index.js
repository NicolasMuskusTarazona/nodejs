function suma(x,y) {
    return x + y
}

function resta(x,y) {
    return x - y
}

function multiplicacion(x,y) {
    return x * y
}

function dividir(x,y) {
    return x / y
}

module.exports ={
    suma,
    resta,
    multiplicacion,
    dividir
}
